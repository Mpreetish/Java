import java.util.*;
class swap{


    //here we swaping the value 
    //the A value change here to B,the B value change here to A
    static void change(int B,int A){
        System.out.println(B);
        System.out.println(A);

    }
 
    public static void main(String[] args){
        int A=10;
        int B=20;
        // We passing the A value first ,Next B value
        //change(A value,B value);
        change(A,B); 

    }

}